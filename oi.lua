local numero = 0

-- enquanto um númeo for menor que 10
-- vai continuar somando até 10, nem mais nem menos

while (numero <10) do 

numero = numero + 1

 print ("Teste ".. numero)
end 




--  em for, o primeiro numero é onde que vai comecar, 
-- o segundo até quanto que vai,
-- e o ultimo quanto que soma a cada numero

for i = 10, 0, -1 do

print ("for ".. i)

end




