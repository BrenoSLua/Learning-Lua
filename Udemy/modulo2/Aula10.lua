--------------------- OPERADORES RELACIONAIS 

-- ==  / ~= 'Diferente' / < menor / > maior / <= menor ou igual / >= maior ou igual
--[[ equivalencia
local var1 = 5 == 5
local var2 = 5 == 6

print(var1, var2)

]]





--[[  diferenncia

local var3 = 5 ~= 5
local var4 = 5 ~= 6 

print(var3, var4)

]]




--[[menor / maior

local var5 = 5 < 5
local var6 = 5 > 6
print (var5, var6)

]]


--[[ menor iguual / maior igual]]

local var7 = 5 <= 5
local var8 = 5 >= 7

print (var7, var8)