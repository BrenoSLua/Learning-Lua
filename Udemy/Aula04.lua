--  Operadores artméticos:
-- ex: -, +, /, *, %, ^

-- +

print(9 + 9)

-- "-"

print(8 - 7)

print(-4)-- aqui diz que este número é negativo

-- * 

print(45 * 4)

-- /

print(10 / 20)
print(10 / 5)

-- % (mostra o resto da divisão)

print(10 % 5) -- ou seja 0

-- ^ (exponeiciação)

print(2^2) --  ou seja 4


